from __future__ import annotations


class DBDataType:
    """
    DBDataTypes contains data source-neutral constants for referring to the basic, common column data types.
    """

    TEXT = "text"
    INTEGER = "integer"
    DECIMAL = "decimal"
    DATE = "date"
    TIME = "time"
    TIMESTAMP = "timestamp"
    TIMESTAMP_TZ = "timestamptz"
    BOOLEAN = "boolean"
